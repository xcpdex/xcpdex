    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $request->validate([
            'q' => 'required',
        ]);

        $results = \Cache::remember('search_' . $request->q, 5, function() use($request) {
            return \App\Asset::where('name', 'like', '%' . $request->q . '%')
                ->withCount('baseMarkets', 'quoteMarkets')
                ->orderBy('volume_total_usd', 'desc')
                ->paginate(50);

        });

        return view('search.index', compact('results', 'request'));
    }

